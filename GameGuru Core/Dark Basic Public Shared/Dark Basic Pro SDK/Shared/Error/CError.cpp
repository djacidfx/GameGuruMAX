void RunTimeError ( DWORD dwErrorCode )
{
	//C++Conversion
	char szErrorString[1024];
    std::string err = "";
    GetRuntimeErrorDescription(dwErrorCode, &err);
	if(strlen(g_strErrorClue)>0)
		sprintf ( szErrorString , "Runtime Error %i: %s (%s)" , dwErrorCode, err.c_str(), g_strErrorClue );
	else
		sprintf ( szErrorString , "Runtime Error %i: %s", dwErrorCode, err.c_str());

	// add LUA trace if any
	if (g_CurrentLuaState)
	{
		lua_Debug ar;
		if (lua_getstack(g_CurrentLuaState, 1, &ar))
		{
			lua_getinfo(g_CurrentLuaState, "Sl", &ar);
			char pLUAErr[1024];
			sprintf(pLUAErr, ". Found in '%s' at line %d.", ar.short_src, ar.currentline);
			strcat(szErrorString, pLUAErr);
		}
		g_CurrentLuaState = nullptr;
	}

	// Ensure any runtime error is added to crash log content
	GG_CRASH_CONTEXT("RunTimeError", "%s", szErrorString);

	#ifndef NOSTEAMORVIDEO
	timestampactivity(0, szErrorString);
	#endif
	if (g_iIgnoreAllErrors == 0)
	{
		MessageBox(NULL, szErrorString, "Error", MB_OK | MB_TOPMOST);
		CrashHandler(NULL);
		//Debug trick , trigger Visual Studio breakpoint. Then use F10 to go to the place the error was generated. also comment out ExitProcess ( 0 );
		__debugbreak();
		ExitProcess(0);
	}
}

