void RunTimeError(DWORD dwErrorCode, LPSTR pExtraErrorString)
{
	if ( pExtraErrorString ) 
	{
		char szErrorString[MAX_PATH];
        std::string err = "";
        GetRuntimeErrorDescription(dwErrorCode, &err);
		if (strlen(g_strErrorClue) > 0)
			sprintf(szErrorString, "Runtime Error %i: %s %s (%s)" , dwErrorCode, err.c_str(), pExtraErrorString, g_strErrorClue );
		else
			sprintf(szErrorString, "Runtime Error %i: %s %s", dwErrorCode, err.c_str(), pExtraErrorString);

		// add LUA trace if any
		if (g_CurrentLuaState)
		{
			lua_Debug ar;
			if (lua_getstack(g_CurrentLuaState, 1, &ar))
			{
				lua_getinfo(g_CurrentLuaState, "Sl", &ar);
				char pLUAErr[1024];
				sprintf(pLUAErr, ". Found in '%s' at line %d.", ar.short_src, ar.currentline);
				strcat(szErrorString, pLUAErr);
			}
			g_CurrentLuaState = nullptr;
		}

		// Ensure any runtime error is added to crash log content
		GG_CRASH_CONTEXT("RunTimeError", "%s", szErrorString);

		#ifndef NOSTEAMORVIDEO
		timestampactivity(0, szErrorString);
		#endif
		if (g_iIgnoreAllErrors == 0)
		{
			MessageBox(NULL, szErrorString, "Error", MB_OK | MB_TOPMOST);
			CrashHandler(NULL);
			__debugbreak();
			ExitProcess(0);
		}
	}
	else
	{
		RunTimeError ( dwErrorCode );
	}
}

void LastSystemError()
{
	LPVOID lpBuffer;
	if (g_iIgnoreAllErrors == 0)
	{

		if (!FormatMessage(FORMAT_MESSAGE_ALLOCATE_BUFFER | FORMAT_MESSAGE_FROM_SYSTEM | FORMAT_MESSAGE_IGNORE_INSERTS,
			NULL, GetLastError(), MAKELANGID(LANG_NEUTRAL, SUBLANG_DEFAULT),
			(LPTSTR)&lpBuffer, 0, NULL)) return;
		MessageBox(NULL, (LPCTSTR)lpBuffer, "System Error", MB_OK);
		LocalFree(lpBuffer);
	}
}
